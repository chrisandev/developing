'use strict'

/*Funciones matematicas con el objeto Math
El objeto Math se emplea para realizar un sinfin de operaciones matematicas
este objeto es static por lo que no requiere establecerlo previamente en nuestro 
programa ni tampoco un constructor, podemos consultarlo en google poniendo 
javascript math object*/

console.log(Math.random()); //Metodo Random nos devuelve un numero al azar
console.log(Math.random()*100); //Al azar multiplicado por 100
console.log(Math.ceil(Math.random()*100)); /*Metodo ceil nos redondea el numero
al siguiente entero y lo deja como entero solo*/
