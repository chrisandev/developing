'use strict'

/*Objeto Math
este  objeto tiene asociados distintos metodos, para aplicarlos basta con 
instanciar el objeto Math seguido de uno de sus metodos
Hay muchos metodos disponibles que pueden consultarse*/

//Numero aleatorio decimal
console.log(Math.random());

//Numero redondeado hacia arriba y siempre entero, metodo ceil

console.log(Math.ceil(Math.random()*100));

